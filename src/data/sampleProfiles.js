// Helper functions for filtering and searching
export const filterProfilesByRole = (profiles, role) => {
  return profiles.filter(profile => profile.role === role);
};

export const filterProfilesByDepartment = (profiles, department) => {
  return profiles.filter(profile => 
    profile.department.toLowerCase().includes(department.toLowerCase())
  );
};

export const filterProfilesByCollege = (profiles, college) => {
  return profiles.filter(profile => 
    profile.college.toLowerCase().includes(college.toLowerCase())
  );
};

export const filterProfilesBySkill = (profiles, skill) => {
  return profiles.filter(profile => 
    profile.skills.some(s => s.toLowerCase().includes(skill.toLowerCase()))
  );
};

export const searchProfiles = (profiles, query) => {
  const searchTerm = query.toLowerCase();
  return profiles.filter(profile => 
    profile.name.toLowerCase().includes(searchTerm) ||
    profile.username.toLowerCase().includes(searchTerm) ||
    profile.bio.toLowerCase().includes(searchTerm) ||
    profile.department.toLowerCase().includes(searchTerm) ||
    profile.college.toLowerCase().includes(searchTerm) ||
    profile.city.toLowerCase().includes(searchTerm) ||
    profile.skills.some(skill => skill.toLowerCase().includes(searchTerm))
  );
};

// Statistics about the dataset
export const getDatasetStats = () => {
  const students = filterProfilesByRole(placementProfiles, 'Student');
  const guiders = filterProfilesByRole(placementProfiles, 'Guider');
  
  const departments = [...new Set(placementProfiles.map(p => p.department))];
  const colleges = [...new Set(placementProfiles.map(p => p.college))];
  const cities = [...new Set(placementProfiles.map(p => p.city))];
  const allSkills = [...new Set(placementProfiles.flatMap(p => p.skills))];
  
  return {
    totalProfiles: placementProfiles.length,
    students: students.length,
    guiders: guiders.length,
    departments: departments.length,
    colleges: colleges.length,
    cities: cities.length,
    uniqueSkills: allSkills.length,
    averageCGPA: students.reduce((sum, s) => sum + s.cgpa, 0) / students.length,
    departmentBreakdown: departments.map(dept => ({
      department: dept,
      count: filterProfilesByDepartment(placementProfiles, dept).length
    }))
  };
};

export default placementProfiles;
